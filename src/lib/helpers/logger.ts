import { log } from "console";

export function Logger(message?: any, ...optionalParams: any[]) {
  log("");
  log("");
  log("");
  log("");
  log(
    "========================================================================================================================================================================",
  );
  if (optionalParams.length > 0) {
    log(message, ...optionalParams);
  } else {
    log(message);
  }
  log(
    "========================================================================================================================================================================",
  );
  log("");
  log("");
  log("");
  log("");
}
